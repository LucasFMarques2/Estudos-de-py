"""""
Operadores aritiméticos

+ = adição
- = Subtração
* = mutiplicação
/ = divisão
// == divisão inteira
** == numero elevado a outro
% == resto da divisão
"""""

print("Adição +", 10 + 10)
print("Subtração -", 10 - 3)
print("Mutiplicação *", 10 * 10)
print("Divisão /", 10 / 2)
print("Divisão inteira //", 10 // 3)
print("Expoente **", 2 ** 4)
print("Resto da divisão %", 10 % 3)

